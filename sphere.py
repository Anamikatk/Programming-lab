#Volume of Sphere
def vol_sphere():
    print("\n\t------VOLUME OF SPHERE------\n")
    r=int(input("Enter the radius of the circle : "))
    vol=(4/3)*3.14*(r**3)
    print("Volume of the Sphere : ",vol)

#Perimeter of Sphere
def peri_sphere():
    print("\n\t------PERIMETER OF SPHERE------\n")
    r=int(input("Enter the radius of the circle : "))
    peri=6.283*r
    print("Perimeter of the Sphere : ",peri)

