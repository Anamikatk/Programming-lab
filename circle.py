#Area of Circle
def ar_circle():
    print("\n\t------AREA OF CIRCLE------\n")
    r=int(input("Enter the radius of the circle : "))
    ar=3.14*r*r
    print("Area of the circle : ",ar)

#Perimeter of Circle
def peri_circle():
    print("\n\t------PERIMETER OF CIRCLE------\n")
